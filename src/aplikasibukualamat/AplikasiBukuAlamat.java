    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = nameField.getText();
String phone = phoneField.getText();
String address = addressField.getText();

if (!name.isEmpty() && !phone.isEmpty() && !address.isEmpty()) {
    tableModel.addRow(new Object[]{name, phone, address});
    clearFields();
} else {
    JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!");
}


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
if (selectedRow != -1) {
    tableModel.setValueAt(nameField.getText(), selectedRow, 0); // Nama
    tableModel.setValueAt(phoneField.getText(), selectedRow, 1); // Telepon
    tableModel.setValueAt(addressField.getText(), selectedRow, 2); // Alamat
    clearFields();
} else {
    JOptionPane.showMessageDialog(this, "Pilih baris untuk diedit!");
}


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
            int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            tableModel.removeRow(selectedRow);
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris untuk dihapus!");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        // Ambil nilai dari tabel
        String name = tableModel.getValueAt(selectedRow, 0).toString();
        String phone = tableModel.getValueAt(selectedRow, 1).toString();
        String address = tableModel.getValueAt(selectedRow, 2).toString();

        // Set nilai ke JTextField
        nameField.setText(name);
        phoneField.setText(phone);
        addressField.setText(address);
    }
    }//GEN-LAST:event_jTable1MouseClicked

